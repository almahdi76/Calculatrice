package beans;

public class Calcul {
	private double x;
	private double y;
	private double resultat;
	
	public double getX() {
		return x;
	}
	public void setX(double x) {
		this.x = x;
	}
	public double getY() {
		return y;
	}
	public void setY(double y) {
		this.y = y;
	}
	public double getResultat() {
		return resultat;
	}
	public void setResultat(double f) {
		this.resultat = f;
	}
	

}
